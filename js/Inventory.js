const itemsPerPage = 12;
let currentPage = 1;

function renderCars(page, filteredCars = cars) {
    filteredCars.sort((a, b) => a.soldOut - b.soldOut);

    const start = (page - 1) * itemsPerPage;
    const end = start + itemsPerPage;
    const carsToShow = filteredCars.slice(start, end);

    const carList = document.getElementById("car-list");
    carList.innerHTML = ""; // Limpiar autos anteriores

    if (carsToShow.length === 0) {
        carList.innerHTML = `<div class="col-12 text-center mt-4"><p>No matching cars found.</p></div>`;
        updatePagination(0); // No paginaci√≥n si no hay resultados
        return;
    }

    carsToShow.forEach(car => {
        const carDiv = document.createElement("div");
        carDiv.className = "col-md-4 mb-4";
        carDiv.innerHTML = `
            <div class="card" style="height: 100%;">
                <img src="${car.images[0]}" class="card-img-top" alt="${car.title}" title="Click on the image to see more" data-bs-toggle="modal" data-bs-target="#carModal" onclick="openModal(${cars.indexOf(car)})">
                ${car.soldOut ? `<div class="sold-out-banner">SOLD</div>` : ""}
                <div class="card-body">
                    <h5 class="card-title">${car.title}</h5>
                    <p class="card-text">Price: ${car.price}</p>
                    <a href="Pre-approval.php?car=${encodeURIComponent(car.title)}" class="btn btn-primary w-100">Get Pre-Approval</a>
                </div>
            </div>
        `;
        carList.appendChild(carDiv);
    });

    updatePagination(filteredCars.length);
}

function openModal(carIndex) {
    const car = cars[carIndex];
    const carouselInner = document.getElementById("carousel-inner");
    carouselInner.innerHTML = ""; // Limpiar las im√°genes anteriores en el modal

    car.images.forEach((img, index) => {
        const itemDiv = document.createElement("div");
        itemDiv.className = `carousel-item ${index === 0 ? "active" : ""}`;
        itemDiv.innerHTML = `<a href="${img}" target="_blank"><img src="${img}" class="d-block w-100" alt="${car.title}" title="Click here to see full image"></a>`;
        carouselInner.appendChild(itemDiv);
    });
    

    document.getElementById("carModalLabel").textContent = car.title;

    // Mostrar datos del carro en el modal
    document.getElementById("car-price").textContent = car.price;
    document.getElementById("car-year").textContent = car.year;
    document.getElementById("car-mileage").textContent = car.mileage;
    document.getElementById("car-description").textContent = car.description;
    document.getElementById("car-down").textContent = car.down || 'N/A';
    document.getElementById("car-cash").textContent = car.cash || 'N/A';
}

function updatePagination(totalItems = cars.length) {
    const totalPages = Math.ceil(totalItems / itemsPerPage);
    const pagination = document.getElementById("pagination");
    pagination.innerHTML = "";

    if (totalPages <= 1) return;

    for (let i = 1; i <= totalPages; i++) {
        const pageItem = document.createElement("li");
        pageItem.className = `page-item ${i === currentPage ? "active" : ""}`;
        pageItem.innerHTML = `<a class="page-link" href="#" onclick="goToPage(${i})">${i}</a>`;
        pagination.appendChild(pageItem);
    }
}
function goToPage(page) {
    currentPage = page;
    renderCars(currentPage);
}

function filterCars() {
    const selectedMakes = Array.from(document.querySelectorAll('input[name="make"]:checked')).map(cb => cb.value);
    const filteredCars = cars.filter(car => selectedMakes.length === 0 || selectedMakes.includes(car.make));
    renderCars(1, filteredCars);
}

document.querySelectorAll('input[name="make"]').forEach(checkbox => {
    checkbox.addEventListener('change', filterCars);
});


